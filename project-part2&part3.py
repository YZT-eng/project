# ================= 1. Configuration and Environment Setup =================

DATA_DIR = '/content/local_dataset'
SAVE_DIR = '/content/drive/MyDrive/InsPLAD_Stage2_Results_improved'
os.makedirs(SAVE_DIR, exist_ok=True)

DEVICE = torch.device("cuda" if torch.cuda.is_available() else "cpu")
print(f"Using Device: {DEVICE}")

# Hyperparameters
BATCH_SIZE = 32
EPOCHS = 30
LEARNING_RATE = 1e-4

# ================= 2. Data Augmentation and Loading =================

data_transforms = {
    'train': transforms.Compose([
        transforms.Resize((224, 224)),
        transforms.RandomHorizontalFlip(),
        transforms.RandomVerticalFlip(),
        transforms.RandomRotation(15),
        transforms.ColorJitter(brightness=0.2, contrast=0.2),
        transforms.ToTensor(),
        transforms.Normalize([0.485, 0.456, 0.406], [0.229, 0.224, 0.225])
    ]),
    'valid': transforms.Compose([
        transforms.Resize((224, 224)),
        transforms.ToTensor(),
        transforms.Normalize([0.485, 0.456, 0.406], [0.229, 0.224, 0.225])
    ]),
}

print("Loading datasets...")
# Check if the data exists
if not os.path.exists(DATA_DIR):
    raise FileNotFoundError(f"Dataset not found at {DATA_DIR}. Please check your path.")

image_datasets = {x: datasets.ImageFolder(os.path.join(DATA_DIR, x), data_transforms[x])
                  for x in ['train', 'valid']}

dataloaders = {x: DataLoader(image_datasets[x], batch_size=BATCH_SIZE, shuffle=True, num_workers=2)
               for x in ['train', 'valid']}

class_names = image_datasets['train'].classes
dataset_sizes = {x: len(image_datasets[x]) for x in ['train', 'valid']}

# --- Calculate category weights ---
targets = np.array(image_datasets['train'].targets)
class_counts = np.bincount(targets)
weights = 1. / (torch.tensor(class_counts, dtype=torch.float) + 1e-6)
class_weights = (weights / weights.sum() * len(class_names)).to(DEVICE)

# ================= 3. Model Building =================

