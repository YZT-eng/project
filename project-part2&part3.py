# ================= 3. End-to-end system-level statistical operation =================
def run_system_analytics(img_dir):
    # --- Statistical funnel indicators ---
    total_images_processed = 0
    total_assets_detected = 0      # Total assets detected by YOLO
    raw_faults_predicted = 0       # The number of times ResNet's original prediction was "sick".
    logic_corrections = 0          # Number of times intercepted and corrected by business logic
    final_validated_faults = 0     # The final output is the actual number of alarms.

    fault_summary = {name: 0 for name in class_names if name not in ['good', 'normal']}

    img_list = [f for f in os.listdir(img_dir) if f.endswith(('.jpg', '.png'))]

    for img_name in tqdm(img_list, desc="Running System Analytics"):
        img_path = os.path.join(img_dir, img_name)
        img = cv2.imread(img_path)
        if img is None: continue
        total_images_processed += 1
        canvas = img.copy()

        # 1. Target Detection Phase
        results = detector(img_path, conf=0.25, verbose=False)[0]
        has_final_fault = False

        for box in results.boxes:
            total_assets_detected += 1
            asset_cls = detector.names[int(box.cls[0])]
            x1, y1, x2, y2 = map(int, box.xyxy[0])

            # 2. Fault Diagnosis Phase
            roi = img[y1:y2, x1:x2]
            if roi.size == 0: continue
            roi_pil = Image.fromarray(cv2.cvtColor(roi, cv2.COLOR_BGR2RGB))
            input_tensor = preprocess(roi_pil).unsqueeze(0).cuda()

            with torch.no_grad():
                pred_idx = torch.max(classifier(input_tensor), 1)[1].item()
                raw_label = class_names[pred_idx]

            # Record whether the original error was reported.
            if raw_label not in ['good', 'normal']:
                raw_faults_predicted += 1

            # 3. Business logic constraint layer
            allowed_labels = ASSET_FAULT_LOGIC.get(asset_cls, ['good', 'normal'])

            if raw_label in allowed_labels:
                final_label = raw_label
            else:
                final_label = 'good' if 'good' in allowed_labels else 'normal'

                if raw_label not in ['good', 'normal']:
                    logic_corrections += 1

            # 4. Statistical and Visualization Results
            if final_label not in ['good', 'normal']:
                final_validated_faults += 1
                fault_summary[final_label] += 1
                has_final_fault = True

                cv2.rectangle(canvas, (x1, y1), (x2, y2), (0, 0, 255), 3)
                cv2.putText(canvas, f"{asset_cls}:{final_label}", (x1, y1-10),
                            cv2.FONT_HERSHEY_SIMPLEX, 0.6, (0, 0, 255), 2)


        if has_final_fault:
            cv2.imwrite(os.path.join(SAVE_DIR, f"sys_alert_{img_name}"), canvas)

    # ================= 4. Output a system-level analysis report. =================
    print("\n" + "="*60)
    print("🚀 Power Line Inspection System - Analytics Report 🚀")
    print("="*60)
    print(f"Images Processed          : {total_images_processed}")
    print(f"[Stage 1] Assets Detected : {total_assets_detected} (Boxes cropped to ResNet)")
    print(f"[Stage 2] Raw Faults Pred : {raw_faults_predicted} (Before logic filter)")
    print(f"[Stage 3] Logic Intercepts: {logic_corrections} (False alarms prevented)")
    print(f"[Output ] Final Validated Alerts: {final_validated_faults}")
    print("-" * 60)
    print("Breakdown of Validated Faults:")
    for fault, count in fault_summary.items():
        if count > 0:
            print(f"  - {fault:15}: {count}")
    print("="*60)

